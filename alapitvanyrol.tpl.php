<p>Az alapítvány teljes neve: Free Software Foundation Hungary Alapítvány a Szabad Szoftverek Magyarországi Népszerűsítéséért és Honosításáért.</p>
<p>Az FSF jelentése: Free Software Foundation, azaz Szabad Szoftver Alapítvány. Az FSF elnöke Richard Stallman, aki 1984-ben elindította GNU Projektet. Nagyjából ekkor indult a szabad szoftveres mozgalom. A <a href="http://www.gnu.hu/">GNU-kiáltványból</a> többet is megtudhat az ideológiai háttérről. Mindazonáltal az FSF.hu nem az FSF magyarországi fiókszervezete, tőle teljesen független.</p>
<p>A népszerűsítés fogalmát talán nem kell sokat magyarázni. A honosításon lényegében a szabad szoftverek és dokumentációk fordítását értjük angol nyelvről magyarra. Alapelvünk, hogy honosított szoftvernek úgy kell kinéznie, mintha azt eleve a célnyelven írták volna meg.</p>
